package paran.join;

public class StudentDataBean {
	public StudentDataBean(){}
	
	private String id;
	private String name;
	private String pwd;
	private String birth_ymd;
	private String sexual_tp;
	private String addr;
	private String cell_phone;
	

	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getBirth_ymd() {
		return birth_ymd;
	}
	public void setBirth_ymd(String birth_ymd) {
		this.birth_ymd = birth_ymd;
	}
	public String getSexual_tp() {
		return sexual_tp;
	}
	public void setSexual_tp(String sexual_tp) {
		this.sexual_tp = sexual_tp;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getCell_phone() {
		return cell_phone;
	}
	public void setCell_phone(String cell_phone) {
		this.cell_phone = cell_phone;
	}



}
